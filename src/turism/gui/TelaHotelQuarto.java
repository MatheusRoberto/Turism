    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        int cama = Integer.valueOf(txtCamaCasal.getValue().toString());
        quarto.setCamacasal(cama);
        cama = Integer.valueOf(txtCamaSolteiro.getValue().toString());
        quarto.setCamasolteiro(cama);
        quarto.setIdhotel(hotel);
        quarto.setObservacao(jTXAOBS.getText());
        
        if(quarto.getIdquarto() == null){
            qDAO.adiciona(quarto);
        }else{
            qDAO.atualiza(quarto);
        }
        
        OcupanteDAO oDAO = new OcupanteDAO();
        
        if(!oDAO.buscaOcupantes(quarto.getIdquarto()).isEmpty()){
            oDAO.buscaOcupantes(quarto.getIdquarto()).forEach((oc) -> {
                oDAO.apagar(oc);
            });
        }
        
        ocupantes.stream().map((oc) -> {
            oc.setQuarto(quarto);
            return oc;
        }).forEachOrdered((oc) -> {
            oDAO.adicionar(oc);
        });
        
        JOptionPane.showMessageDialog(this, "Quarto e Ocupantes adicinado com Sucesso!s");
    }//GEN-LAST:event_btnSalvarActionPerformed

