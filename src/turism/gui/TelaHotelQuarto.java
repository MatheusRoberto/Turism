    private void jCBDepedenteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCBDepedenteItemStateChanged
        // TODO add your handling code here:
        if (jCBDepedente.isSelected() == true) {
            System.err.println(cliente.getIdcliente());
            if (cliente.getIdcliente() != null) {
                painelDepedente.setVisible(true);
                depedentes.clear();
                DepedenteDAO dDAO = new DepedenteDAO();
                depedentes = dDAO.buscaDepedenteCliente(cliente.getIdcliente());
                modeloDepedente.limpar();
                modeloDepedente.addListaDepedente(depedentes);
            } else {
                JOptionPane.showMessageDialog(this, "Selecione um cliente!");
            }
        } else {
            painelDepedente.setVisible(false);
            depedentes.clear();
        }
    }//GEN-LAST:event_jCBDepedenteItemStateChanged

    private void btnAdicionarOcupanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarOcupanteActionPerformed
        // TODO add your handling code here:
        Ocupantes ocupante = new Ocupantes();
        OcupantesPK opk = new OcupantesPK();

        if (contrato.getIdcontrato() != null && hotel.getIdhotel() != null) {
            opk.setContratoIdcontrato(contrato.getIdcontrato());
            ocupante.setOcupantesPK(opk);
            ocupante.setContrato(contrato);
            ocupante.setQuarto(quarto);
            if (jCBDepedente.isSelected()) {
                int indice = jtDepedente.getSelectedRow();
                if (indice > -1) {
                    ocupante.setNome(depedentes.get(indice).getNome());
                } else {
                    ocupante.setNome(cliente.getNome());
                }
            } else {
                ocupante.setNome(cliente.getNome());
            }
            ocupantes.add(ocupante);
            modeloOcupante.addItem(ocupante);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente!");
        }
    }//GEN-LAST:event_btnAdicionarOcupanteActionPerformed

