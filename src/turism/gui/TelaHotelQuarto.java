    private void listBuscaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listBuscaMouseClicked
        // TODO add your handling code here:
        int indice = listBusca.getSelectedIndex();
        if (indice >= 0) {
            cliente = clientes.get(indice);
            ContratoDAO ctDAO = new ContratoDAO();
            ArrayList<Contrato> contratos = ctDAO.buscaContratoViagem(hotelcontratado.getViagem().getIdviagem());
            contratos.stream().filter((c) -> (c.getIdcliente().equals(cliente))).forEachOrdered((c) -> {
                contrato = c;
            });
            DepedenteDAO dDAO = new DepedenteDAO();
            depedentes = dDAO.buscaDepedenteCliente(cliente.getIdcliente());
        }
        jBusca.setVisible(false);
        this.chaveBackground(true);
        txtCliente.setText(cliente.getNome());
        jCBDepedente.setSelected(true);
        jCBDepedenteItemStateChanged(null);
    }//GEN-LAST:event_listBuscaMouseClicked

