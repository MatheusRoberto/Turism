    private void txtBuscaDepedenteCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtBuscaDepedenteCaretUpdate
        // TODO add your handling code here:
        depedentes = dDAO.buscaDepedente(txtBuscaDepedente.getText());
        listBuscaDepedente.removeAll();
        DefaultListModel modelol = new DefaultListModel();
        depedentes.forEach((next) -> {
            modelol.addElement(next.getNome());
        });
        listBuscaDepedente.setModel(modelol);
        listBuscaDepedente.repaint();
    }//GEN-LAST:event_txtBuscaDepedenteCaretUpdate

