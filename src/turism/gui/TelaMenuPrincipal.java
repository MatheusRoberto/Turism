    private void abreJInternal(JInternalFrame inte) {
        JInternalFrame interno = inte;
        interno.setLocation((this.getSize().width) / 2, (this.getSize().height) / 2);
        interno.setVisible(true);
        interno.setResizable(false);
        interno.toFront();
    }

    private void abreJFrame(JFrame tela) {
        JFrame janela = tela;
        janela.setVisible(true);
        janela.setResizable(false);
        janela.setLocationRelativeTo(null);
        janela.toFront();
    }
